package ch.bfh.swos.jmusic.service.dto;

public class GenreDTO {

	private Long id;
	private String name;

	public GenreDTO() {
		super();
	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
